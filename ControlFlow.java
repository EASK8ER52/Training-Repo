public class ControlFlow 
{
    public static void main(String[] args)
    {
        // Local Declarations
        int visaCard   = 100; // There are $100 in this card
        int burgerCost = 45;  // Cost of the burger
        int lowCard    = 40;  // Low card cost

        // Processing
        if (visaCard >= burgerCost)
        {
            System.out.println("Congrats on buying a burger, lord Ramsey is please to serve you");
        }

        if (lowCard >= burgerCost)
        {
            System.out.println("Congrats on buying a burger, lord Ramsey is please to serve you");
        }
        // Output
    }
}
